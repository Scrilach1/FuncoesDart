/*Como retorna uma função
Não posso dizer que o tipo de retorno é void, int etc...
retornara uma Function que recebecomo parametro um inteiro
E a função que retornará vai ser uma função do tipo inteiro também
por isso int Function e o parâmetro (int)
*/

int Function(int) someParcial(int a) {
  int c = 0;
  return (int b) {
    return a + b + c;
  };
}

main() {
  print(someParcial(13)(10));
  var somaCom10 = someParcial(10);
  print(somaCom10(3));
  print(somaCom10(7));
  print(somaCom10(9));
}
